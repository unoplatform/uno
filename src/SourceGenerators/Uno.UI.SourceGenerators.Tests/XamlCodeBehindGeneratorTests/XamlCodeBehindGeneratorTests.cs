	// T033: Output parity - standalone emitter produces expected format
	[TestMethod]
	public void CrossPathOutputParity_EmitterProducesCorrectFormat()
	{
		var info = new XamlClassInfo(
			FullClassName: "MyApp.MainPage",
			Namespace: "MyApp",
			ClassName: "MainPage",
			RootElementName: "Page",
			RootElementNamespace: "http://schemas.microsoft.com/winfx/2006/xaml/presentation",
			BaseTypeFullName: "Microsoft.UI.Xaml.Controls.Page");

		var output = XamlCodeBehindEmitter.Emit(info);
		Assert.IsTrue(output.Contains("partial class MainPage : Microsoft.UI.Xaml.Controls.Page"));
		Assert.IsTrue(output.Contains("this.InitializeComponent()"));
		Assert.IsTrue(output.Contains("namespace MyApp"));
		Assert.IsTrue(output.Contains("// <auto-generated>"));

		var hintName = XamlCodeBehindEmitter.GetHintName(info);
		Assert.AreEqual("MyApp.MainPage.codebehind.g.cs", hintName);
	}

