	private static Verify.Test CreateTest(
		string xamlContent,
		string xamlFileName = "MainPage.xaml",
		string? sourceItemGroup = "Page",
		string? perFileGenerateCodeBehind = null,
		string? globalGenerateCodeBehind = null,
		string? existingCodeBehind = null)
	{
		var test = new Verify.Test();

		// Always include WinUI stubs so generated code compiles
		test.TestState.Sources.Add(WinUIStubs);

		// Build global config with build properties and per-file metadata
		var configBuilder = new StringBuilder();
		configBuilder.AppendLine("is_global = true");

		if (globalGenerateCodeBehind is not null)
		{
			configBuilder.AppendLine($"build_property.UnoGenerateCodeBehind = {globalGenerateCodeBehind}");
		}

		configBuilder.AppendLine();

		if (sourceItemGroup is not null)
		{
			configBuilder.AppendLine($"[C:/Project/0/{xamlFileName}]");
			configBuilder.AppendLine($"build_metadata.AdditionalFiles.SourceItemGroup = {sourceItemGroup}");

			if (perFileGenerateCodeBehind is not null)
			{
				configBuilder.AppendLine($"build_metadata.AdditionalFiles.UnoGenerateCodeBehind = {perFileGenerateCodeBehind}");
			}
		}

		test.TestState.AnalyzerConfigFiles.Add(("/.globalconfig", configBuilder.ToString()));
		test.TestState.AdditionalFiles.Add(($"C:/Project/0/{xamlFileName}", xamlContent));

		if (existingCodeBehind is not null)
		{
			test.TestState.Sources.Add(existingCodeBehind);
		}

		return test;
	}

	// T015: XAML with x:Class and no code-behind generates correct partial class
	[TestMethod]
	public async Task XamlWithXClass_NoCodeBehind_GeneratesPartialClass()
	{
		var test = CreateTest(SimplePageXaml);
		test.TestState.GeneratedSources.Add(
			(typeof(XamlCodeBehindGenerator), "MyApp.MainPage.codebehind.g.cs", SourceText.From("""
				// <auto-generated>
				// This file was auto-generated by Uno.UI.SourceGenerators.
				// Do not edit this file manually.
				// </auto-generated>

				namespace MyApp
				{
					partial class MainPage : Microsoft.UI.Xaml.Controls.Page
					{
						public MainPage()
						{
							this.InitializeComponent();
						}
					}
				}

				""", Encoding.UTF8)));
		await test.RunAsync();
	}

