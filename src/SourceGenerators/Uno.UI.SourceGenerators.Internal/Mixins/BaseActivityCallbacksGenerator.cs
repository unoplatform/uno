	public void Initialize(IncrementalGeneratorInitializationContext context)
	{
		var platformProvider = context.AnalyzerConfigOptionsProvider.Select(static (options, ct) =>
		{
			options.GlobalOptions.TryGetValue("build_property.DefineConstantsProperty", out var constants);
			return constants?.Contains("__ANDROID__") == true;
		});

		context.RegisterSourceOutput(platformProvider, static (ctx, isAndroid) =>
		{
			if (!isAndroid)
			{
				return;
			}

			ctx.AddSource("BaseActivity.Callbacks.g.cs", Generate());
		});
	}

	public static string Generate()
	{
		var sb = new StringBuilder();
		sb.AppendLine("// <auto-generated>");
		sb.AppendLine("// This file was generated by BaseActivityCallbacksGenerator. Do not edit manually.");
		sb.AppendLine("// </auto-generated>");
		sb.AppendLine();
		sb.AppendLine("#if UNO_MIXIN_GENERATION && __ANDROID__");
		sb.AppendLine("#pragma warning disable");
		sb.AppendLine();
		sb.AppendLine("using System;");
		sb.AppendLine("using Android.App;");
		sb.AppendLine("using Android.Content;");
		sb.AppendLine("using Android.Content.PM;");
		sb.AppendLine("using Android.Content.Res;");
		sb.AppendLine("using Android.Graphics;");
		sb.AppendLine("using Android.OS;");
		sb.AppendLine("using Android.Util;");
		sb.AppendLine("using Android.Views;");
		sb.AppendLine("using Java.Lang;");
		sb.AppendLine("using Uno.UI.ViewManagement;");
		sb.AppendLine();
		sb.AppendLine("namespace Uno.UI");
		sb.AppendLine("{");
		sb.AppendLine("\tpartial class BaseActivity : IBaseActivityEvents { }");
		sb.AppendLine();

		var methodRegex = new Regex(@"(?<modifiers>[\s\w]+)\s+(?<return>[\w\.]+)\s+(?<name>\w+)\((?<parameters>[^\)]*)\)(;(?<nameOverride>.*))?");

		var separators = new[] { '\r', '\n' };
		var methods = OverridableMethods
			.Split(separators, StringSplitOptions.None)
			.Where(line => !string.IsNullOrWhiteSpace(line) && !line.Trim().StartsWith("//", StringComparison.Ordinal) && line.Contains("virtual"))
			.Distinct()
			.Select(method =>
			{
				var match = methodRegex.Match(method);
				var name = match.Groups["name"].Value;
				var nameOverride = match.Groups["nameOverride"].Value;
				var eventName = string.IsNullOrEmpty(nameOverride) ? name : nameOverride;
				eventName = eventName.StartsWith("On", StringComparison.Ordinal) ? eventName.Substring(2) : eventName;

				return new
				{
					Modifiers = match.Groups["modifiers"].Value.Trim(),
					ReturnType = match.Groups["return"].Value,
					Name = name,
					EventName = eventName,
					NameOverride = nameOverride,
					Parameters = match.Groups["parameters"].Value
				};
			})
			.OrderBy(m => m.Name)
			.ThenBy(m => m.Parameters.Length)
			.ToArray();

		foreach (var method in methods)
		{
			var name = string.IsNullOrEmpty(method.NameOverride)
				? method.Name.Replace("On", "")
				: method.NameOverride;

			var parameters = method.Parameters
				.Split(',')
				.Where(p => !string.IsNullOrWhiteSpace(p))
				.Select(p =>
				{
					var parts = p.Split(' ').Where(part => !string.IsNullOrWhiteSpace(part)).ToArray();
					return new { Type = parts.FirstOrDefault() ?? "NOT_FOUND", ParamName = parts.LastOrDefault() ?? "NOT_FOUND" };
				})
				.ToArray();

			var baseParameters = string.Join(",", parameters.Select(p => p.ParamName));

			string type, value;
			if (parameters.Length == 0)
			{
				type = "";
				value = "";
			}
			else if (parameters.Length == 1)
			{
				type = parameters[0].Type;
				value = parameters[0].ParamName;
			}
			else
			{
				type = name + "EventArgs";
				value = "new " + name + "EventArgs(" + baseParameters + ")";

				// Generate EventArgs class
				sb.AppendLine("\tpartial class BaseActivity");
				sb.AppendLine("\t{");
				sb.AppendLine($"\t\tpublic class {name}EventArgs");
				sb.AppendLine("\t\t{");
				sb.AppendLine($"\t\t\tpublic {name}EventArgs({method.Parameters})");
				sb.AppendLine("\t\t\t{");
				foreach (var p in parameters)
				{
					var propName = char.ToUpper(p.ParamName[0], System.Globalization.CultureInfo.InvariantCulture) + p.ParamName.Substring(1);
					sb.AppendLine($"\t\t\t\t{propName} = {p.ParamName}; ");
				}
				sb.AppendLine("\t\t\t}");
				sb.AppendLine();
				foreach (var p in parameters)
				{
					var propName = char.ToUpper(p.ParamName[0], System.Globalization.CultureInfo.InvariantCulture) + p.ParamName.Substring(1);
					sb.AppendLine($"\t\t\tpublic {p.Type} {propName} {{ get; }} ");
				}
				sb.AppendLine("\t\t}");
				sb.AppendLine("\t}");
			}

			// Generate event declaration
			sb.AppendLine("\tpartial class BaseActivity");
			sb.AppendLine("\t{");
			sb.AppendLine("\t\t/// <summary>");
			sb.AppendLine($"\t\t/// Occurs when <see cref=\"{method.Name}\"/> is invoked on the activity.");
			sb.AppendLine("\t\t/// </summary>");
			if (!string.IsNullOrEmpty(type))
			{
				sb.AppendLine($"\t\tpublic event EventHandler<{type}> {name};");
			}
			else
			{
				sb.AppendLine($"\t\tpublic event EventHandler {name};");
			}
			sb.AppendLine("\t}");

			// Generate override method
			var overrideModifiers = method.Modifiers.Replace("virtual", "override");
			sb.AppendLine();
			sb.AppendLine("\tpartial class BaseActivity");
			sb.AppendLine("\t{");
			sb.AppendLine($"\t\t{overrideModifiers} {method.ReturnType} {method.Name}({method.Parameters})");
			sb.AppendLine("\t\t{");
			sb.AppendLine($"\t\t\tvar handler = {name};");
			sb.AppendLine("\t\t\tif (handler != null)");
			sb.AppendLine("\t\t\t{");
			sb.AppendLine($"\t\t\t\thandler.Invoke(this, {(string.IsNullOrEmpty(value) ? "EventArgs.Empty" : value)});");
			sb.AppendLine("\t\t\t}");
			sb.AppendLine();
			sb.AppendLine($"\t\t\t_{method.EventName}Handlers?.Invoke({baseParameters});");
			sb.AppendLine();
			sb.AppendLine($"\t\t\tInner{name}({baseParameters});");
			sb.AppendLine($"\t\t\t{(method.ReturnType == "void" ? "" : "return ")}base.{method.Name}({baseParameters});");
			sb.AppendLine("\t\t}");
			sb.AppendLine();
			sb.AppendLine($"\t\tpartial void Inner{name}({method.Parameters});");
			sb.AppendLine();
			sb.AppendLine($"\t\tprivate Activity{method.EventName}Handler _{method.EventName}Handlers;");
			sb.AppendLine($"\t\tevent Activity{method.EventName}Handler IBaseActivityEvents.{method.EventName}");
			sb.AppendLine("\t\t{");
			sb.AppendLine("\t\t\tadd");
			sb.AppendLine("\t\t\t{");
			sb.AppendLine($"\t\t\t\t_{method.EventName}Handlers += value;");
			sb.AppendLine("\t\t\t}");
			sb.AppendLine("\t\t\tremove");
			sb.AppendLine("\t\t\t{");
			sb.AppendLine($"\t\t\t\t_{method.EventName}Handlers -= value;");
			sb.AppendLine("\t\t\t}");
			sb.AppendLine("\t\t}");
			sb.AppendLine("\t}");
			sb.AppendLine();
		}

		sb.AppendLine("}");
		sb.AppendLine("#endif ");

		return sb.ToString();
	}

