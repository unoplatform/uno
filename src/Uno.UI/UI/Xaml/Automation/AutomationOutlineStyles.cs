namespace Microsoft.UI.Xaml.Automation;

/// <summary>
/// Specifies the outline style of text.
/// </summary>
public enum AutomationOutlineStyles
{
	/// <summary>
	/// No outline style is applied.
	/// </summary>
	None = 0,

	/// <summary>
	/// Text is displayed with an outline.
	/// </summary>
	Outline = 1,

	/// <summary>
	/// Text is displayed with a shadow.
	/// </summary>
	Shadow = 2,

	/// <summary>
	/// Text is displayed with an engraved effect.
	/// </summary>
	Engraved = 3,

	/// <summary>
	/// Text is displayed with an embossed effect.
	/// </summary>
	Embossed = 4,
}
