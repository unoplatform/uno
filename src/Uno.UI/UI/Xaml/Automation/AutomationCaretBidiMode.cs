namespace Microsoft.UI.Xaml.Automation;

/// <summary>
/// Specifies the bidirectional (bidi) mode of the caret.
/// </summary>
public enum AutomationCaretBidiMode
{
	/// <summary>
	/// The caret is in left-to-right (LTR) mode.
	/// </summary>
	LTR = 0,

	/// <summary>
	/// The caret is in right-to-left (RTL) mode.
	/// </summary>
	RTL = 1,
}
